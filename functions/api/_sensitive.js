function normalize(text) {
    if (!text) return '';
    return text
        .toLowerCase()
        .replace(/[\s​‌‍﻿]/g, '')
        .replace(/[·•．.。，,！!？?、\-_~`@#$%^&*()+=\[\]{}|\\/<>]/g, '')
        .replace(/0/g, 'o')
        .replace(/1/g, 'i')
        .replace(/3/g, 'e')
        .replace(/4/g, 'a')
        .replace(/5/g, 's');
}

export function checkText(text) {
    if (!text) return { pass: true };
    const normalized = normalize(text);
    for (const word of ALL_WORDS) {
        const normalizedWord = normalize(word);
        if (normalized.includes(normalizedWord)) {
            return { pass: false, reason: '内容包含敏感信息，无法发布' };
        }
    }
    return { pass: true };
}

export async function checkImage(env, imageData) {
    if (!env || !env.AI) return { pass: true };
    if (!imageData || !imageData.startsWith('data:image')) return { pass: true };

    try {
        const base64 = imageData.split(',')[1];
        if (!base64) return { pass: true };

        const binaryStr = atob(base64);
        const bytes = new Uint8Array(binaryStr.length);
        for (let i = 0; i < binaryStr.length; i++) {
            bytes[i] = binaryStr.charCodeAt(i);
        }

        const result = await env.AI.run('@cf/microsoft/resnet-50', {
            image: [...bytes]
        });

        if (result && Array.isArray(result)) {
            const nsfwLabels = ['bikini', 'brassiere', 'miniskirt', 'maillot', 'nipple', 'nude', 'porn', 'sex'];
            for (const item of result) {
                const label = (item.label || '').toLowerCase();
                const score = item.score || 0;
                if (score > 0.5 && nsfwLabels.some(n => label.includes(n))) {
                    return { pass: false, reason: '图片包含不适当内容，无法发布' };
                }
            }
        }
        return { pass: true };
    } catch (e) {
        return { pass: true };
    }
}
